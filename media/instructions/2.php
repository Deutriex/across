<p><h1>3 - Dicas</h1></p>

<ul>
<li>Se a imagem for específica a uma marca (Coca-cola, Unilever, etc.) a resposta é a prória marca.</li>
<li>Se a imagem for específica a um artista famoso (Michael Jackson, Selena Gomez, etc.), a resposta é o próprio nome do arquivo</li>
<li>A resposta jamais conterá mais de duas palavras, entretanto respostas de duas palavras são menos prováveis de estarem corretas do que respostas de uma palavra</li>
<li>A resposta jamais será o nome genérico. Se você reponder animal, árvore, imóvel... Você irá errar questões de teste e poderá ser desqualificado</li>
<li>A resposta semper está de acordo com a norma culta da Língua Portuguesa. Se enviar respostas como masieira, gatu...  Você irá errar questões de teste e poderá ser desqualificado</li>
<li>Suas respostas serão convertidas em minúsculas a fim de te ajudar para que suas respostas sejam validadas com sucesso</li>  
</ul>
